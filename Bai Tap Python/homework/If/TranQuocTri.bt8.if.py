tuoi=int(input("Nhap vao so tuoi: "))
if tuoi>0 and tuoi<=2:
    print("Tre so sinh")
elif tuoi > 2 and tuoi <=10 :
    print("Nhi dong")
elif tuoi>10 and tuoi <= 17:
    print("Vi thanh nien")
elif tuoi>17 and tuoi<= 39:
    print("Thanh nien")
elif tuoi>39 and tuoi<= 50:
    print("Trung nien")
elif tuoi>50:
    print("Cao nien")
